export type Language = 'en' | 'ar';
export type TranslationKeys = keyof typeof translations.en;

export const getTranslation = (lang: Language, section: string, key: string): string => {
  try {
    const langObj = translations[lang];
    const sectionObj = langObj[section as keyof typeof langObj];
    if (sectionObj && typeof sectionObj === 'object') {
      return sectionObj[key as keyof typeof sectionObj] || key;
    }
    return key;
  } catch {
    return key;
  }
};

export const t = (lang: Language) => (section: string, key: string): string => {
  return getTranslation(lang, section, key);
};
